Map::Map()
{
	textureMap[0] = TextureManager::LoadTexture("assets/dirt.png");
	textureMap[1] = TextureManager::LoadTexture("assets/grass.png");
	textureMap[2] = TextureManager::LoadTexture("assets/stone.png");
	textureMap[3] = TextureManager::LoadTexture("assets/water.png");
	LoadMap(tempMap);

	src.x = 0;
	src.y = 0;
	src.h = dest.h = 32;
	src.w = dest.w = 32;

	dest.x = dest.y = 0;
}

Map::~Map()
{	
}

void Map::LoadMap(int mapArr[20][25]) {
	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			map[i][j] = mapArr[i][j];
		}
	}
}

void Map::DrawMap() {
	SDL_Texture* tileType = 0;
	for (int i = 0; i < 20; i++)
	{
		for (int j = 0; j < 25; j++)
		{
			tileType = textureMap[map[i][j]];
			dest.x = j * 32;
			dest.y = i * 32;
			TextureManager::Draw(tileType, src, dest);
		}
	}
}